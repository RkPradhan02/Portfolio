var contentHeight = $("#content")[0].scrollHeight; //the real height of the content

var ratio = (contentHeight-300)/250;

$(".scrollbar-thumb").draggable({  //drag function
  containment:"parent",
  axis:"y",
  drag:function () {
    var scrollbarTop = $(".scrollbar-thumb").position().top-20;
    $("#content").css({
"top":-scrollbarTop*ratio
});
  }
});

      
function mouseWheelHandler (event) {  //function during mousewheel event
  var event = event;
  var delta = event.wheelDelta; //the distance of mousewheel scroll
  var scrollbarTop = Math.max(0, Math.min(250, $(".scrollbar-thumb").position().top - 20 - delta*.1)); //get the scrollbar top value between 0 and 250
  
  $(".scrollbar-thumb").css({
"top":scrollbarTop
});
  
  $("#content").css({
"top":-scrollbarTop*ratio
});
}

if (window.addEventListener) {
  window.addEventListener("mousewheel", mouseWheelHandler);  //for IE9, Chrome, Safari, Opera
  window.addEventListener("DOMMouseScroll", mouseWheelHandler);  //for  Firefox
} else {
  window.attachEvent("onmousewheel", mouseWheelHandler);  //for IE 6/7/8
}

